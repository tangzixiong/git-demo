## python sys 模块

#http://www.ityouknow.com/python/2019/10/09/python-sys-demonstration-028.html


#“sys”即“system”，“系统”之意。该模块提供了一些接口，用于访问 Python 解释器自身
# 使用和维护的变量，同时模块中还提供了一部分函数，可以与解释器进行比较深度的交互

## sys.argv
# argv即argument value的简写，是一个列表对象，其中
# 存储的是在命令行调用python脚本时提供的命令行参数


##sys.platform 属性可以得到关于运行平台更详细的信息


##sys.byteorder “byteorder”即“字节序”，指的是在计算机内部
#  存储数据时，数据的低位字节存储在存储空间中的高位还是低位。


## sys.executable 该属性是一个字符串，在正常情况下，其值是
# 当前运行的 Python 解释器对应的可执行程序所在的绝对路径